    private void bRealizar3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRealizar3ActionPerformed
        // TODO add your handling code here:
        String frase = tfCadena.getText();
        frase = frase.replace(" ", "");
        frase = frase.toLowerCase();
        char letras[] = frase.toCharArray();
        
        Map<Character, Integer> countMap = new HashMap();
        for (char letra : letras){
            try{
                countMap.put(letra,countMap.get(letra)+1);
            }catch(NullPointerException npe){
                countMap.put(letra, 1);
            }
        }
        char letraModa;
        letraModa = Collections.max(countMap.entrySet(), Comparator.comparingInt(Map.Entry::getValue)).getKey();
        JOptionPane.showMessageDialog(null,"La letra de moda en el texto ingresado es: " + letraModa + ", con una frecuencia de: " + countMap.get(letraModa));

        countMap.keySet().forEach((letra) -> {
        JOptionPane.showMessageDialog(null, "Letra [" + letra + "]: " + countMap.get(letra));
});
        
        
    }//GEN-LAST:event_bRealizar3ActionPerformed

